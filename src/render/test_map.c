void free_test_map(t_game *game)
{
    size_t i = 0;

    while(game->map_info->temp_map[i] != NULL)
    {
        free(game->map_info->temp_map[i]);
        i++;
    }
    free(game->map_info->temp_map);
    game->map_info->temp_map = NULL;
}
